package br.com.tin.model;

import java.io.Serializable;


public class Multiplos implements Serializable {
    
    private int multiplo;
    private int soma;
    private int nmult;

    public int getMultiplo() {
        return multiplo;
    }

    public void setMultiplo(int multiplo) {
        this.multiplo = multiplo;
    }

    public int getSoma() {
        return soma;
    }

    public void setSoma(int soma) {
        this.soma = soma;
    }

    public int getNmult() {
        return nmult;
    }

    public void setNmult(int nmult) {
        this.nmult = nmult;
    }

    
    
}


